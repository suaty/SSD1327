void SSD1327_ClearDisplay(void) {
    uint8_t buffer[SSD1327_WIDTH * SSD1327_HEIGHT / 2] = {0X00}; // 4-bit per pixel
    memset(buffer, 0x00, sizeof(buffer));
    SSD1327_WriteCommand(SSD1327_SET_COLUMN_ADDRESS);
    SSD1327_WriteCommand(0x00); // Start column
    SSD1327_WriteCommand((SSD1327_WIDTH / 2) - 1); // End column

    SSD1327_WriteCommand(SSD1327_SET_ROW_ADDRESS);
    SSD1327_WriteCommand(0x00); // Start row
    SSD1327_WriteCommand(SSD1327_HEIGHT - 1); // End row

    SSD1327_WriteData(buffer, sizeof(buffer));
}

