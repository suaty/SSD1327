void SSD1327_DisplayText(const char *text, uint8_t x, uint8_t y, uint8_t *displayBuffer, const uint8_t font[][8]) {

    while (*text) {
        char c = *text++;  // Sradaki karakteri al

        // ASCII karakterinin geerli olup olmadn kontrol et
        if (c < 32 || c > 127) {
            c = '?';  // Geersiz karakterler iin '?' gster
        }

        // Font tablosundan karakter bitmap'ini al
        const uint8_t *charBitmap = font[c - 32];

        // Karakteri buffer'a yaz
        for (uint8_t row = 0; row < 8; row++) { // 8 satr iin
            uint8_t line = charBitmap[row];    // Bitmap'teki satr verisi
            for (uint8_t col = 0; col < 8; col++) { // Her stun iin
                uint8_t pixelVal = (line & (1 << (7 - col))) ? 0xF : 0x0; // 4-bit gri tonlama (ak = 0xF, kapal = 0x0)

                // Buffer'daki uygun indeksi bul
                uint16_t index = ((y + row) * SSD1327_WIDTH + (x + col)) / 2;

                // Piksel buffer'da hangi nibble'da (yksek/alt) yer alacak?
                if ((x + col) % 2 == 0) {
                    displayBuffer[index] = (displayBuffer[index] & 0x0F) | (pixelVal << 4); // st nibble
                } else {
                    displayBuffer[index] = (displayBuffer[index] & 0xF0) | pixelVal; // Alt nibble
                }
            }
        }

        // Bir sonraki karakterin balang x koordinatn ayarla
        x += 8;
        if (x >= SSD1327_WIDTH) {
            x = 0;      // Eer ekrann sonuna ulaldysa, satr bana ge
            y += 8;     // Bir satr aa in
            if (y >= SSD1327_HEIGHT) {
                return; // Eer ekrann sonuna ulaldysa, yazmay durdur
            }
        }
    }
}

