void SSD1327_SetCursor(uint8_t x, uint8_t y) {
	SSD1327_WriteCommand(0x15); // Set Column Address
	SSD1327_WriteCommand(x);
	SSD1327_WriteCommand(0x7F); // End at 127
	SSD1327_WriteCommand(0x75); // Set Row Address
	SSD1327_WriteCommand(y);
	SSD1327_WriteCommand(0x7F); // End at 127
}

void send_command(uint8_t cmd) {
	HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_RESET); // CS pinini LOW yap
	HAL_GPIO_WritePin(OLED_DC_GPIO_Port, OLED_DC_Pin, GPIO_PIN_RESET);  // DC pinini LOW yap (komut modu)
    HAL_SPI_Transmit(&hspi2, &cmd, 1, HAL_MAX_DELAY);     // SPI ile komut gnder
    HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_SET);   // CS pinini HIGH yap
}

void send_data(uint8_t data) {
	HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_RESET); // CS pinini LOW yap
	HAL_GPIO_WritePin(OLED_DC_GPIO_Port, OLED_DC_Pin, GPIO_PIN_SET);   // DC pinini HIGH yap (veri modu)
    HAL_SPI_Transmit(&hspi2, &data, 1, HAL_MAX_DELAY);    				// SPI ile veri gnder
    HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_SET);   // CS pinini HIGH yap
}

void set_cursor(uint8_t col, uint8_t row) {
    // SSD1327 adresleme komutlar
    uint8_t start_column = col * 2;  // 4-bit gri tonlama iin her stun 2 bayt
    uint8_t end_column = start_column + 1;

    send_command(0x15);             // Set Column Address
    send_command(start_column);     // Start Column
    send_command(end_column);       // End Column

    send_command(0x75);             // Set Row Address
    send_command(row);              // Start Row
    send_command(row + 7);          // End Row (8-pixel height)
}

void drawLine(int x0, int y0, int x1, int y1) {
    int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = -abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = dx + dy, e2; // Error value e_xy

    while (1) {
        //setPixel(buffer, x0, y0); // Set the pixel
        SSD1327_DrawPixel(x0, y0, 0x0F);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; } // Step x
        if (e2 <= dx) { err += dx; y0 += sy; } // Step y
    }
}

void drawChar(char c, uint8_t x, uint8_t y) {
    if (c < 0 || c > 127) return; // Geersiz karakter kontrol

    c = c-32;
    for (uint8_t i = 0; i < 8; i++) {
    	uint8_t line = font8x8_basic[(uint8_t)c][i];
        for (uint8_t j = 0; j < 8; j++) {
            if (line & (1 << j)) {
                SSD1327_DrawPixel(x + i, y + j, 0x0F);
            }
        }
    }
}

void drawChar16(char c, uint8_t x, uint8_t y) {
    if (c < 0 || c > 127) return; // Geersiz karakter kontrol

    const uint16_t *bitmap = &font16x16[c][0];

    for (uint8_t i = 0; i < 16; i++) {
        uint16_t line = bitmap[i];
        line = ((line & 0x5555) << 1) | ((line & 0xAAAA) >> 1); // Bitleri ters evir
        line = ((line & 0x3333) << 2) | ((line & 0xCCCC) >> 2);
        line = ((line & 0x0F0F) << 4) | ((line & 0xF0F0) >> 4);
        line = (line << 8) | (line >> 8);

        for (uint8_t j = 0; j < 16; j++) {
            if (line & (1 << (15 - j))) { // Eer piksel set edilmise
                SSD1327_DrawPixel(x + j, y + i, 1); // Pikseli izer
            } else {
                SSD1327_DrawPixel(x + j, y + i, 0); // Pikseli temizler
            }
        }
    }
}

void drawString(const char *str, uint8_t x, uint8_t y) {
    while (*str) {
        drawChar(*str, x, y);
        x += 8; // Her karakter 8 piksel geniliinde olduundan sonraki konum
        str++;
    }
}

