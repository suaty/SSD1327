void SSD1327_DrawPixel(uint8_t x, uint8_t y, uint8_t color) {

    // Koordinat snrlarn kontrol et
    if (x >= SSD1327_WIDTH || y >= SSD1327_HEIGHT) return;

    // Pikselin tampon bellek indeksini hesapla
    uint16_t index = (x / 2) + (y * (SSD1327_WIDTH / 2));

    // Pikseli yaz (x koordinatna gre sol/sa bitler)
    if (x % 2 == 0) {
        // Sol piksel (baytn st 4 biti)
        displayBuffer[index] = (displayBuffer[index] & 0x0F) | (color << 4);
    } else {
        // Sa piksel (baytn alt 4 biti)
        displayBuffer[index] = (displayBuffer[index] & 0xF0) | (color & 0x0F);
    }

}

